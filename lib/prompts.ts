export function getPromptByReportType(reportType: ReportType, inputText: string) {
  const format = REPORT_OUTPUT_FORMATS[reportType];
  const rules = REPORT_RULES[reportType].map((rule) => `- ${rule}`).join("\n");

  return [
    `선택된 보고서 종류: ${reportType}`,
    "",
    "추가 작성 지시:",
    rules,
    "- 지정된 항목명과 순서를 유지할 것",
    '- 입력에 없는 정보는 반드시 "없음"으로 작성할 것',
    "- 형식 외의 서론, 결론, 해설은 출력하지 말 것",
    "",
    "반드시 아래 형식으로만 출력:",
    format,
    "",
    "입력 원문:",
    inputText,
  ].join("\n");
}
