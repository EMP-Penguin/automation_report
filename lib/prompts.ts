import { type ReportType } from "@/lib/report-config";

const COMMON_RULES = [
  "아래 형식에 주어진 지침과 예시에 맞게 작성할 것",
  '주어지지 않은 내용은 "없음"이라고 작성할 것',
  '문체는 반드시 "-ㅁ, -음"으로 끝낼 것',
  "형식 외의 설명 문장 출력 금지",
  "설명 없이 결과만 출력할 것",
];

function buildCommonSection() {
  return [
    "공통 규칙:",
    ...COMMON_RULES.map((rule) => `- ${rule}`),
    "",
    "누락 정보 처리 규칙:",
    '- 입력에 없는 내용, 확정할 수 없는 내용, 교수 설명이 없는 내용은 반드시 "없음"으로 작성할 것',
    "- 추정하거나 상상해서 보완하지 말 것",
    "",
    "출력 형식 고정:",
    "- 지정된 항목명 순서 유지",
    "- 각 항목은 한 줄로 시작하고 필요한 경우 같은 줄에서 이어서 작성",
    "- 서론, 결론, 제목, 해설, 주석, 마크다운 코드블록 출력 금지",
  ].join("\n");
}

export function getPromptByReportType(reportType: ReportType, inputText: string) {
  const commonSection = buildCommonSection();

  switch (reportType) {
    case "병동 회진 참관 보고서":
      return `역할 지정:
당신은 병동 회진 참관 내용을 의과대학 실습 보고서 형식으로 정리하는 한국어 작성 도우미임.

${commonSection}

보고서 작성 규칙:
- 병동 회진 참관 보고서는 500bytes 이내로 작성할 것
- 환자의 최종 진단명 또는 주진단명을 결정하게 된 근거를 포함할 것
- 환자의 증상 또는 신체검진 소견 또는 혈액검사 상 양성 소견을 포함할 것
- 감별진단 2개를 반드시 포함할 것

출력 형식:
- 환자 정보:
- 진단명:
- 증상:
- 감별진단:

입력 원문:
${inputText}`;

    case "외래 참관 보고서":
      return `역할 지정:
당신은 외래 참관 내용을 의과대학 실습 보고서 형식으로 정리하는 한국어 작성 도우미임.

${commonSection}

보고서 작성 규칙:
- 외래 참관 보고서는 500bytes 이내로 작성할 것
- 환자의 최종 진단명 또는 주진단명을 결정하게 된 근거를 포함할 것
- 환자의 증상, 신체검진 소견을 포함하고 무증상이면 risk factor 또는 history 위주로 작성할 것
- 감별진단 2개를 반드시 포함할 것

출력 형식:
- 환자 정보:
- 진단명:
- 증상:
- 감별진단:

입력 원문:
${inputText}`;

    case "시술/검사 참관 보고서":
      return `역할 지정:
당신은 시술 또는 검사 참관 내용을 의과대학 실습 보고서 형식으로 정리하는 한국어 작성 도우미임.

${commonSection}

보고서 작성 규칙:
- 시술/검사 참관 보고서는 500bytes 이내로 작성할 것
- 시술/검사의 전체 적응증을 포함할 것
- 이 환자에 해당하는 적응증을 포함할 것
- 금기증을 포함할 것
- 합병증을 포함할 것

출력 형식:
- 환자 정보:
- 적응증:
- 이 환자에 해당하는 적응증:
- 금기증:
- 합병증:

입력 원문:
${inputText}`;

    case "수술 참관 보고서":
      return `역할 지정:
당신은 수술 참관 내용을 의과대학 실습 보고서 형식으로 정리하는 한국어 작성 도우미임.

${commonSection}

보고서 작성 규칙:
- 환자 정보에는 이름, 나이, 진단명, 수술명을 포함할 것
- 수술의 목적 또는 수술 시 고려해야 할 주의사항을 포함할 것
- 수술실에서 교수님이 시행한 설명 또는 교수님과 질의응답 내용을 포함할 것
- 수술을 보면서 학생이 느낀 점을 포함할 것

출력 형식:
- 환자 정보 (이름, 나이, 진단명, 수술명):
- 수술의 목적 또는 수술 시 고려해야 할 주의사항:
- 수술실에서 교수님이 시행한 설명 또는 교수님과 질의응답 내용:
- 수술을 보면서 학생이 느낀 점:

입력 원문:
${inputText}`;
  }
}
